package com.example.csvtodb;

public class BookingInfo {
    private Integer srNo;
    private String zone;
    private String typeOfBooking;
    private String developersName;
    private String projectName;
    private String unit;
    private Double carpetArea;
    private String wingTower;
    private String seriesNo;
    private String dateOfVisitRevisit;
    private String dateOfBooking;
    private String carParking;


    public Integer getSrNo() { return srNo; }
    public void setSrNo(Integer srNo) { this.srNo = srNo; }
    public String getZone() { return zone; }
    public void setZone(String zone) { this.zone = zone; }
    public String getTypeOfBooking() { return typeOfBooking; }
    public void setTypeOfBooking(String typeOfBooking) { this.typeOfBooking = typeOfBooking; }
    public String getDevelopersName() { return developersName; }
    public void setDevelopersName(String developersName) { this.developersName = developersName; }
    public String getProjectName() { return projectName; }
    public void setProjectName(String projectName) { this.projectName = projectName; }
    public String getUnit() { return unit; }
    public void setUnit(String unit) { this.unit = unit; }
    public Double getCarpetArea() { return carpetArea; }
    public void setCarpetArea(Double carpetArea) { this.carpetArea = carpetArea; }
    public String getWingTower() { return wingTower; }
    public void setWingTower(String wingTower) { this.wingTower = wingTower; }
    public String getSeriesNo() { return seriesNo; }
    public void setSeriesNo(String seriesNo) { this.seriesNo = seriesNo; }
    public String getDateOfVisitRevisit() { return dateOfVisitRevisit; }
    public void setDateOfVisitRevisit(String dateOfVisitRevisit) { this.dateOfVisitRevisit = dateOfVisitRevisit; }
    public String getDateOfBooking() { return dateOfBooking; }
    public void setDateOfBooking(String dateOfBooking) { this.dateOfBooking = dateOfBooking; }
    public String getCarParking() { return carParking; }
    public void setCarParking(String carParking) { this.carParking = carParking; }
}