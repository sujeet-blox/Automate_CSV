package com.example.csvtodb;

public class FollowUpDetails {
    private String remarks;
    private String spotReward;
    private String baseIncentiveStatus;
    private String masterCurrentStatus;
    private String nextFollowUpDate;

    // Getters and Setters
    public String getRemarks() { return remarks; }
    public void setRemarks(String remarks) { this.remarks = remarks; }
    public String getSpotReward() { return spotReward; }
    public void setSpotReward(String spotReward) { this.spotReward = spotReward; }
    public String getBaseIncentiveStatus() { return baseIncentiveStatus; }
    public void setBaseIncentiveStatus(String baseIncentiveStatus) { this.baseIncentiveStatus = baseIncentiveStatus; }
    public String getMasterCurrentStatus() { return masterCurrentStatus; }
    public void setMasterCurrentStatus(String masterCurrentStatus) { this.masterCurrentStatus = masterCurrentStatus; }
    public String getNextFollowUpDate() { return nextFollowUpDate; }
    public void setNextFollowUpDate(String nextFollowUpDate) { this.nextFollowUpDate = nextFollowUpDate; }
}
