package com.example.csvtodb;

public class SourcingCredits {
    private String sourcingCreditsDept;
    private String sourcingManager;
    private String portfolioRmClosingCredits;
    private String rmTl;
    private String rmHead;

    // Getters and Setters
    public String getSourcingCreditsDept() { return sourcingCreditsDept; }
    public void setSourcingCreditsDept(String sourcingCreditsDept) { this.sourcingCreditsDept = sourcingCreditsDept; }
    public String getSourcingManager() { return sourcingManager; }
    public void setSourcingManager(String sourcingManager) { this.sourcingManager = sourcingManager; }
    public String getPortfolioRmClosingCredits() { return portfolioRmClosingCredits; }
    public void setPortfolioRmClosingCredits(String portfolioRmClosingCredits) { this.portfolioRmClosingCredits = portfolioRmClosingCredits; }
    public String getRmTl() { return rmTl; }
    public void setRmTl(String rmTl) { this.rmTl = rmTl; }
    public String getRmHead() { return rmHead; }
    public void setRmHead(String rmHead) { this.rmHead = rmHead; }
}
